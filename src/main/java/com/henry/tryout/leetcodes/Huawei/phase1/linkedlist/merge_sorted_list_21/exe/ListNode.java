package com.henry.tryout.leetcodes.Huawei.phase1.linkedlist.merge_sorted_list_21.exe;

public class ListNode {

    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}
